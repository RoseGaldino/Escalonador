    private void jButtonExecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecActionPerformed
        
        if(Radio_FIFO.isSelected() && thread_escalonador.isAlive()==false){
            FIFO fifo = new FIFO(proc, Label_mostrarLetra, Label_mostrarProcessos, Label_mostrarTempo);
            thread_escalonador = new Thread((Runnable) fifo);
          
            thread_escalonador.start();

        }
        else if(Radio_RR.isSelected() && thread_escalonador.isAlive()==false){
            quantum = SeletorQuantum.getValue(); 
            ROBIN robin = new ROBIN(proc, Label_mostrarLetra, Label_mostrarProcessos, Label_mostrarTempo, quantum);
            thread_escalonador = new Thread(robin);
            thread_escalonador.start();

        }
        else if(Radio_SJF.isSelected() && thread_escalonador.isAlive()==false){
            SJF sjf = new SJF(proc, Label_mostrarLetra, Label_mostrarProcessos, Label_mostrarTempo);
            thread_escalonador = new Thread(sjf);
            thread_escalonador.start();

        }
    }//GEN-LAST:event_jButtonExecActionPerformed

