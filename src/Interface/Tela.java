    private void jButtonCarregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCarregarMouseClicked
    
        if(thread_escalonador.isAlive() == false)
        {
            proc.clear(); 
           
            String diretorioString = System.getProperty("user.dir");
            diretorioString = diretorioString.replace("\\", "/"); 
            diretorioString += "/src/Tela/";

            Path path = Paths.get(diretorioString+"processos.txt");
            List <String> valoresProcessos = new ArrayList<>();
            try {
                valoresProcessos = Files.readAllLines(path, StandardCharsets.UTF_8);
            } catch (IOException ex) {
                Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
            }

            int tempo = 0; 
            int  cont = 0; 
           
            for(String processo : valoresProcessos)  {
               
                tempo = Integer.parseInt(processo);                
              
                String nomeProcesso = Character.toString((char)(65+cont));
                proc.add(new Processo(nomeProcesso,tempo));
                cont++;
            }
          
            Label_mostrarProcessos.setText("<html>"+proc.toString()+"<html>");
        }
    }//GEN-LAST:event_jButtonCarregarMouseClicked

